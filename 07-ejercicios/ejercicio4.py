"""
Pedir dos numeros al usuario y hacer todas las operaciones
basicas de una calculadora y mostrarlo por pantalla.
"""

numero_1 = int(input("Introduce el primero un número "))
numero_2 = int(input("Introduce el segundo un número "))

print(f"{numero_1} + {numero_2} = {numero_1+numero_2}")
print(f"{numero_1} - {numero_2} = {numero_1-numero_2}")
print(f"{numero_1} x {numero_2} = {numero_1*numero_2}")
print(f"{numero_1} / {numero_2} = {numero_1/numero_2}")
