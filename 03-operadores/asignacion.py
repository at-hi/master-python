#Operadores de asignacion: Permitene asignar un valor a una variable

edad = 50 #El = le da un valor a una variable
print(edad)

edad += 5 #Le suma 5 al valor que ya tiene la variable edad (55)
edad -= 5 #Reasigna el valor de la variable y le resta 5
print(edad)

#Operadores de incremento y decremento

year = 2021

#Incremento
year = year + 1
print(year)

#Decremento
year = year - 1
print(year)

#Pre-Incremento
year = 1 + year
print(year)

#Pre-Decremento
year = 1 - year
print(year)