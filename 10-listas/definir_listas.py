"""
LISTAS (arrays) se definen con [] corchetes
Colecciones de datos/valores, bajo un único nombre
Para acceder a esos valores podemos usar un indice numérico
"""

pelicula = "Batman"
print(pelicula)


#2 formas para definir una lista
peliculas = ["Batman", "Spiderman", "El señor de los anillos"] #Esta es la forma mas fácil
cantantes = list(("2 pac", "Drake", "Jennifer López")) #Con doble () se usa con una tupla
years = list(range(2020, 2050))
variada = ["Annie", 30, 40.4, True, "Texto"]


print(peliculas)
print(cantantes)
print(years)
print(variada)

