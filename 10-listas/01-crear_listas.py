"""
LISTAS (arrays) se definen con [] corchetes
Colecciones de datos/valores, bajo un único nombre
Para acceder a esos valores podemos usar un indice numérico
"""

pelicula = "Batman"

# Diferentes maneras para defirnir listas
peliculas = ["Batman", "Spiderman", "Shreck"]

#Para que de esta manera funcione, hay que pasarle una tupla, se encierra entre () de nuevo
cantantes = list(("drake", "Juan Luis Gerra", "2-pac"))

# Definir una lista con un rango, una colección de números
years = list(range(2020,2050))

# Definir lista variada
variada = ["Andy", 30, 4.0, 10, True, "Texto"]

print(peliculas)
print(cantantes)
print(years)
print(variada)